
// Make a Calculator! using prompt(), and variables, make a program that does the following:

var firstNumber = Number(prompt("What is your first number?: "));
var secondNumber = Number(prompt("What is your second number?: "));
alert("Your sum is: " + (firstNumber + secondNumber));
alert("Your subtraction is: " + (firstNumber - secondNumber));
alert("Your multiplication is: " + (firstNumber * secondNumber));
alert("Your division is: " + (firstNumber / secondNumber));